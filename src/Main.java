public class Main {

    public static void main(String[] args) {
        for (int i = 0; i < ascii().length(); i++)
            System.out.print(ascii().charAt(i));
    }
    public static String ascii() {
        return "  ／l、             \n" +
                "（ﾟ､ ｡ ７         \n" +
                "  l  ~ヽ       \n" +
                "  じしf_,)ノ";
    }
}